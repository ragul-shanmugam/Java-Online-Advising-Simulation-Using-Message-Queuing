    /*Method to connect this client to the MQ Server*/
    public void connect(int serverport, String advisor) throws IOException {
        // getting localhost ip 
        InetAddress ip = InetAddress.getByName("localhost");
        // establish the connection 
        Socket socket = new Socket(ip, serverport);

        // obtaining input and out streams 
        dis = new DataInputStream(socket.getInputStream());
        dos = new DataOutputStream(socket.getOutputStream());
        dos.writeUTF(advisor);

    }

    /*Method to generate the random decision of the advisor process*/
    public String randomStringGenerator() {
        Random random = new Random();
        randomDecision = decisionsOfAdvisor[random.nextInt(decisionsOfAdvisor.length)];
        return randomDecision;
    }

