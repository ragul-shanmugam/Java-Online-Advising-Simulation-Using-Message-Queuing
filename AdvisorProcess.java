    public AdvisorProcess() {
        initComponents();
        // readMessage thread 
        Thread readMessage = new Thread(new Runnable() {

            @Override
            public void run() {
                while (true) {
                    try {

                        // read the message sent to this client 
                        String msg = dis.readUTF();

                        /*Reading message from the Server and if the Student Details Map is empty it will
                        sleep for 3 seconds and poll the server again for the new requests from the students*/
                        if ("wait".equals(msg)) {
                            jTextArea1.append("No requests from students found\n");
                            Thread.sleep(3000);
                            dos.writeUTF(advisor);
                        } 
                        else {
                            // break the string into message and recipient part 
                            st = new StringTokenizer(msg, "/");
                            while (st.hasMoreTokens()) {
                                studentName = st.nextToken();
                                courseName = st.nextToken();
                                randomDecision = randomStringGenerator();
                                String finalstr = studentName + "/" + courseName + "#" + randomDecision;
                                jTextArea1.append("Decision for the request from student " + studentName + " is " + randomDecision + "\n");
                                dos.writeUTF(finalstr);
                                
                                //After writing the decision it is polling the Server again 
                                //for the new values from students
                                Thread.sleep(3000);
                                dos.writeUTF(advisor);
                                break;
                            }
                        }
                    } catch (Exception e) {
                    }
                }
            }
        });
        readMessage.start();
    }

